package com.limelight.limelight.models;

public class Clickbait {
    private float news;
    private float clickbait;
    private String message;

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public float getNews() {
        return news;
    }

    public void setNews(float news) {
        this.news = news;
    }

    public float getClickbait() {
        return clickbait;
    }

    public void setClickbait(float clickbait) {
        this.clickbait = clickbait;
    }
}
